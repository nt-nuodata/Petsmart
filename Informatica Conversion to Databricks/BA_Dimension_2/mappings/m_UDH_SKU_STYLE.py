df_4=spark.sql("""
    SELECT
        MASTER.PRODUCT_ID AS PRODUCT_ID1,
        MASTER.SKU_NBR AS SKU_NBR1,
        MASTER.STYLE_ID AS STYLE_ID,
        MASTER.STYLE_DESC AS STYLE_DESC,
        MASTER.IMG_SKU_NBR AS IMG_SKU_NBR,
        MASTER.IMG_PRODUCT_ID AS IMG_PRODUCT_ID,
        MASTER.LOAD_TSTMP AS LOAD_TSTMP,
        DETAIL.PRODUCT_ID AS PRODUCT_ID,
        DETAIL.SKU_NBR AS SKU_NBR,
        DETAIL.SKU_DESC AS SKU_DESC,
        MASTER.Monotonically_Increasing_Id AS Monotonically_Increasing_Id 
    FROM
        SQ_Shortcut_to_UDH_SKU_STYLE_PRE_1 MASTER 
    LEFT JOIN
        SQ_Shortcut_To_SKU_PROFILE_3 DETAIL 
            ON MASTER.PRODUCT_ID = DETAIL.PRODUCT_ID""")

