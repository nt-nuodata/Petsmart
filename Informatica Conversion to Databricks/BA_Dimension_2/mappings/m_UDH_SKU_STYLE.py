df_5=spark.sql("""
    SELECT
        IFF(ISNULL(PRODUCT_ID1),
        PRODUCT_ID,
        PRODUCT_ID1) AS o_PRODUCT_ID,
        IFF(ISNULL(PRODUCT_ID1),
        SKU_NBR,
        SKU_NBR1) AS o_SKU_NBR,
        IFF(ISNULL(PRODUCT_ID1),
        SKU_NBR,
        STYLE_ID) AS o_STYLE_ID,
        IFF(ISNULL(PRODUCT_ID1),
        SKU_DESC,
        STYLE_DESC) AS o_STYLE_DESC,
        IFF(ISNULL(PRODUCT_ID1),
        SKU_NBR,
        IMG_SKU_NBR) AS o_IMG_SKU_NBR,
        IFF(ISNULL(PRODUCT_ID1),
        PRODUCT_ID,
        IMG_PRODUCT_ID) AS o_IMG_PRODUCT_ID,
        IFF(ISNULL(PRODUCT_ID1),
        SYSTIMESTAMP(),
        LOAD_TSTMP) AS o_LOAD_TSTMP,
        Monotonically_Increasing_Id AS Monotonically_Increasing_Id 
    FROM
        JNR_SKU_PROFILE_4""")

df_5.createOrReplaceTempView("EXP_STYLE_SKU_5")

